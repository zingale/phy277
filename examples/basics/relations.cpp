#include <iostream>

int main() {

    double x = 1.2;
    double y = 2.0;

    std::cout << "x > y is " << (x > y) << std::endl;
    std::cout << "x < y is " << (x < y) << std::endl;
    std::cout << "x == y is " << (x == y) << std::endl;
    std::cout << "x != y is " << (x != y) << std::endl;


}
